    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //        ambil value pada masing-masing text-field
        String kodeProduk = txtKodeProduk.getText();
        String namaProduk = txtNamaProduk.getText();
        String kategoriProduk = cbxKategoriProduk.getSelectedItem().toString();
//        parsing data dari string ke integer
       
        Integer hargaProduk = Integer.parseInt(txtHargaProduk.getText());
        
//        panggil fungsi insert data       
        pm.insertData(kodeProduk, namaProduk, kategoriProduk, hargaProduk);
        showTable();
        clear();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

