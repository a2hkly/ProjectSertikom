    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Integer id = Integer.parseInt(tblProduk.getValueAt(tblProduk.getSelectedRow(), 0).toString());
        
        String kodeProduk = txtKodeProduk.getText();
        String namaProduk = txtNamaProduk.getText();
        String kategoriProduk = cbxKategoriProduk.getSelectedItem().toString();
        Integer hargaProduk = Integer.parseInt(txtHargaProduk.getText());
        
                
        pm.updateData(id,kodeProduk, namaProduk, kategoriProduk, hargaProduk);
        showTable();
        clear();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

